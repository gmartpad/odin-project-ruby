# Addition
a = 1 + 1 #=> 2
puts a

# Subtraction
b = 2 - 1 #=> 1
puts b

# Multiplication
c = 2 * 2 #=> 4
puts c

# Division
d = 10 / 5 #=> 2
puts d

# Exponent
e = 2 ** 2 #=> 4
f = 3 ** 4 #=> 81
puts e
puts f

# Modulus (fin the remainder of division)
g = 8 % 2 #=> 0 (8 / 2 = 4; no remainder)
h = 10 % 4 #=> 2 (10 / 4 = 2 with a remainder of 2)
puts g
puts h